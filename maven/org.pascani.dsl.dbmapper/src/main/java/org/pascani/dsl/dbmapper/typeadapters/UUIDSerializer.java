package org.pascani.dsl.dbmapper.typeadapters;

import java.lang.reflect.Type;
import java.util.UUID;

import com.google.gson.JsonElement;
import com.google.gson.JsonPrimitive;
import com.google.gson.JsonSerializationContext;
import com.google.gson.JsonSerializer;

/**
 * Custom Gson serializer for the UUID class
 * 
 * @author Miguel Jiménez - Initial contribution and API
 */
public class UUIDSerializer implements JsonSerializer<UUID> {

	/*
	 * (non-Javadoc)
	 * 
	 * @see com.google.gson.JsonSerializer#serialize(java.lang.Object,
	 * java.lang.reflect.Type, com.google.gson.JsonSerializationContext)
	 */
	@Override public JsonElement serialize(UUID src, Type typeOfSrc,
			JsonSerializationContext context) {
		return new JsonPrimitive(src.toString());
	}

}
